from numpy import sqrt, cbrt, floor
from sympy import symbols

x = symbols('x')

# --- #

# * EXAMPLES
# Horizontal line
y = 2 

# Linear
y = x

# Quadratic
y = x**2

# Cubic
y = x**3

# Quartic
y = x**4

# Quintic
y = x**5

# Absolute value
y = abs(x)

# Square root
y = sqrt(x)

# Square root with rational exponent
y = x**(1/2)

# Cube root
y = cbrt(x)

# Floor
y = floor(x)

# Exponential
y = 2**x

